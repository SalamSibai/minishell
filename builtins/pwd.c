#include "../includes/minishell.h"

/**
 * @brief this function will printed the current working directory 
 * @return itll return 1 on succes and zero on failure
 */
int		ft_pwd(void)
{
	char	cwd[PATH_MAX];

	if (getcwd(cwd, PATH_MAX))
	{
		ft_putendl_fd(cwd, 1);
		return (1);
	}
	else
		return (0);
}